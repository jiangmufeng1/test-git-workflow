/****************************************************************************************************
 * 函数名称： void LedPoint(u16 uiCycle)
 * 入口参数：u16 uiCycle
 * 返回  值：无
 * 功能说明：led周期闪烁处理函数，在定时器中调用，uiCycle的值 = 定时器中断的周期
 ***************************************************************************************************/
void LedPoint(uint16_t uiCycle)
{
	static uint16_t uiCnt = 0;
	
	uiCnt ++;
	if (uiCnt == uiCycle)
	{
		uiCnt = 0;
		HAL_GPIO_TogglePin(GPIOC,GPIO_PIN_13);  // LED = LED^1;
	}
}

